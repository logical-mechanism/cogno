export interface IconProps extends SVGProps<SVGSVGElement> {
  /** Filled variant for stateful icons (like/nav). Ignored by single-variant icons. */
  filled?: boolean;
  /** px or any CSS length; defaults to 1em (inherits font-size). */
  size?: number | string;
}

function svgProps(p: IconProps): SVGProps<SVGSVGElement> {
  const { filled: _filled, size, style, ...rest } = p;
  const dim = size != null ? (typeof size === "number" ? `${size}px` : size) : "1em";
  const merged: CSSProperties = { width: dim, height: dim, ...style };
  return {
    viewBox: "0 0 24 24",
    // Decorative by default (hidden); when a caller opts in with an aria-label, expose it AND set
    // role="img" — a bare <svg aria-label> without role="img" is dropped by some screen readers.
    "aria-hidden": rest["aria-label"] ? undefined : true,
    role: rest["aria-label"] ? "img" : undefined,
    focusable: false,
    fill: "currentColor",
    style: merged,
    ...rest,
  };
}

